# Example of a while loop in Python
count = 1
while count < 11:
    print(count)
    count += 1

# Exercises:
# 1. Prompt the user for a number, then print all numbers from 1 up to that number:
number = int(input("Enter a number: "))
start = 1
while start <= number:
    print(start)
    start += 1